%%%-------------------------------------------------------------------
%%% webgnosus text processing utilities
%%%-------------------------------------------------------------------
-module(webgnosus_text).

%% API
-export([
          split/1
        ]).

%%====================================================================
%% API
%%====================================================================
%%--------------------------------------------------------------------
%% Func: split/1
%% Description: return list of document words.
%%--------------------------------------------------------------------
split(Doc) ->
    Doc.
  

%%====================================================================
%%% Internal functions
%%====================================================================
%%--------------------------------------------------------------------
%% Func: pad_punctuation/1
%% Description: .
%%--------------------------------------------------------------------
pad_punctuation(Doc, Punc) ->    
    case regexp:gsub(Doc, Punc ++ " ", " " ++ Punc) of
        {ok, NewDoc, _} ->
            NewDoc;
        X -> 
            X
    end.
