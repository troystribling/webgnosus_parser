-module(webgnosus_util).

%% API
-export([
          get_attribute/2
        ]).

%%====================================================================
%% API
%%====================================================================
%%--------------------------------------------------------------------
%% Func: get_location_from_headers/1
%% Description: get location from 301 header.
%%--------------------------------------------------------------------
get_attribute(Key, R) ->
    case lists:keysearch(Key, 1, R) of
        {value, {Key, Location}} ->
            Location;
        _ ->
            error
    end.
